package com.infybuzz.request;

public class Status {
	
	private String status;
	private String des;
	
	public Status(String status, String des) {
		super();
		this.status = status;
		this.des = des;
	}

	public String getStatus() {
		return status;
	}

	public void setStatus(String status) {
		this.status = status;
	}

	public String getDes() {
		return des;
	}

	public void setDes(String des) {
		this.des = des;
	}
	
	
	
	

}
